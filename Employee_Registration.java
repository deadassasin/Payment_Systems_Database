    public void employee_regi(){
        
    
        Connection emp_regi = null;
        PreparedStatement prst = null;
        
        
         try{
            
        Class.forName("com.mysql.jdbc.Driver");
           
           String query = "insert into employee_list values (?, ?, ? , ? , ?, ?, ?)";
           emp_regi = DriverManager.getConnection("jdbc:mysql://localhost:3306/paymentsys", "root", "");
           prst = emp_regi.prepareStatement(query); 
           
         
          prst.setString(1,Name.getText());
          prst.setString(2,Address.getText());
          prst.setString(3,Country.getText());
          prst.setString(4,Postal.getText());
          prst.setString(5,Refno.getText());
          prst.setString(6,Employer.getText());
          prst.setString(7,Phone.getText());
          
          int j = prst.executeUpdate();
          
          if(j>0){
              JOptionPane.showMessageDialog(null, "Employee has been added to the list");
          }
          else{
              JOptionPane.showMessageDialog(null, "Employee has NOT been added to the list!");
          }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }    
